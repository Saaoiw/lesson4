public class Laboratory4_5 {
    public static void main(String[] args) {
        int i = 10;
        int j = 20;

        System.out.println( ( i << 2) * j );
        System.out.println( ( i >> 2) * j );
        System.out.println( ( i >>> 3) * j);
    }
}
